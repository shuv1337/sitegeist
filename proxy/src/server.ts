app.listen(config.port, () => {
  log({
    level: "info",
    time: new Date().toISOString(),
    event: "server_start",
    port: config.port,
    allowedHosts: config.allowedHosts,
    authRequired: config.proxySecret !== null,
    rateLimitRpm: config.rateLimitRpm,
  });
});
